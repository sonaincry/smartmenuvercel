class Box {
  constructor(layerId, boxPositionX, boxPositionY, boxWidth, boxHeight, boxMaxCapacity) {
    this.layerId = layerId;
    this.boxPositionX = boxPositionX;
    this.boxPositionY = boxPositionY;
    this.boxWidth = boxWidth;
    this.boxHeight = boxHeight;
    this.boxMaxCapacity = boxMaxCapacity;
  }
}

export default Box;
